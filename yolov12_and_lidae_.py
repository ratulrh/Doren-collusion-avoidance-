def count_split(split_name):
    img_dir = os.path.join(dataset_dir, split_name, "images")
    lbl_dir = os.path.join(dataset_dir, split_name, "labels")

    images = []
    for ext in ("*.jpg", "*.jpeg", "*.png", "*.bmp", "*.webp"):
        images += glob.glob(os.path.join(img_dir, ext))

    label_files = glob.glob(os.path.join(lbl_dir, "*.txt"))

    counter = Counter()
    total_instances = 0

    for lf in label_files:
        with open(lf, "r") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                cls_id = int(line.split()[0])
                counter[cls_id] += 1
                total_instances += 1

    return len(images), len(label_files), total_instances, counter

splits = ["train", "valid", "test"]
