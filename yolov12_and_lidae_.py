import numpy as np

def get_lidar_simulated():
    angles = np.linspace(0,360,360)
    distances = np.random.uniform(3,40,360)
    return angles, distances

print("✔ LiDAR simulation ready.")

def fuse_yolo_lidar(yolo_objects, lidar_angles, lidar_distances):
    fused = []

    for obj in yolo_objects:
        angle = int(obj["center_x"] % 360)
        distance = float(lidar_distances[angle])

        fused.append({
            "class": obj["class"],
            "distance": distance,
            "bbox": obj["bbox"],
            "confidence": obj["confidence"]
        })
    return fused

def collision_risk(fused_objects):
    risks = []
    for o in fused_objects:
        if o["class"] in [0,1,2]:  # birds/drones classes from dataset
            if o["distance"] < 12:  # danger zone
                risks.append(o)
    return risks

import torch
import torch.nn as nn

class LLMFusion(nn.Module):
    def __init__(self, input_dim=5, actions=5):
        super().__init__()
        self.encoder = nn.TransformerEncoder(
            nn.TransformerEncoderLayer(
                d_model=input_dim, nhead=1, batch_first=True),
            num_layers=2
        )
        self.fc = nn.Linear(input_dim, actions)

    def forward(self, x):
        x = self.encoder(x)
        out = self.fc(x.mean(dim=1))
        return out

llm = LLMFusion()
print("✔ LLM Fusion Model Ready")

def llm_decision(llm, obj):
    # Input vector
    x = torch.tensor([[
        [obj["class"], obj["distance"], 0, 0, 0]
    ]], dtype=torch.float)

    out = llm(x)
    action = torch.argmax(out).item()

    actions_map = {
        0: "SAFE → Go forward",
        1: "AVOID LEFT",
        2: "AVOID RIGHT",
        3: "ASCEND quickly",
        4: "DESCEND quickly"
    }
    return actions_map[action]

