def get_yolo_detections(frame):
    results = model(frame)[0]
    objects = []

    for b in results.boxes:
        x1,y1,x2,y2 = b.xyxy[0].cpu().numpy()
        cls = int(b.cls[0])
        conf = float(b.conf[0])
        cx = (x1 + x2) / 2

        objects.append({
            "bbox": (x1,y1,x2,y2),
            "class": cls,
            "confidence": conf,
            "center_x": cx
        })
    return objects

